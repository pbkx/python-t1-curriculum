x = 5 # integer: whole numbers without a decimal
print(x)

favorite_food = "pizza" # string: text enclosed in quotes
print(favorite_food)

print(x, favorite_food) # You can print multiple things on one line by using a comma
print("My favorite food is", favorite_food)

y = 3.25 # float: number with a decimal point
print(y, "dogs", 4)